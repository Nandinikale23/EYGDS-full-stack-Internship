def add(a, b):
    return a + b
def sub(a, b):
    return a - b
def mul(a, b):
    return a * b
def div(a, b):
    return a / b

x = int(input("Enter first number: "))
y = int(input("Enter second number: "))

print("Add:", add(x, y))
print("Subtract:", sub(x, y))
print("Multiply:", mul(x, y))
print("Divide:", div(x, y))
